public class Chocolatina {
    private String marca; // Marca de la chocolatina

    // Constructor que asigna la marca
    public Chocolatina(String marca) {
        this.marca = marca;
    }

    // Método para obtener la marca de la chocolatina
    public String getMarca() {
        return marca;
    }

    // Método para cambiar la marca de la chocolatina
    public void setMarca(String marca) {
        this.marca = marca;
    }
}
